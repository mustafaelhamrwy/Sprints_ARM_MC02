/*******************************************************************************
 *                      Functions Definitions                                  *
 *******************************************************************************/


void ADC_init(ADC_ConfigType *ADC_config_ptr)
{
	/* ADMUX Register Bits Description:
	 * REFS1:0 = 00 to choose to connect external reference voltage by input this voltage through AREF pin
	 * ADLAR   = 0 right adjusted
	 * MUX4:0  = 00000 to choose channel 0 as initialization
	 */
	ADMUX = ((ADC_config_ptr->ref_volt)<<6) | (ADMUX & 0x3f);


	/* ADCSRA Register Bits Description:
	 * ADEN    = 1 Enable ADC
	 * ADIE    = 0 Disable ADC Interrupt
	 * ADATE   = 0 Disable Auto Trigger
	 * ADPS2:0 = 011 to choose ADC_Clock=F_CPU/8=1Mhz/8=125Khz --> ADC must operate in range 50-200Khz
	 */
	ADCSRA = ((ADC_config_ptr->prescaler)| (ADCSRA & 0xf8));
	ADCSRA = (1<<7); /* enable ADC */
}

uint16 ADC_readChannel(Adc_Channel channel_num)
{
	ADMUX = channel_num | (ADMUX & 0xf8);

	SET_BIT(ADCSRA,ADSC); /* Start conversion write '1' to ADSC */
	while(BIT_IS_CLEAR(ADCSRA,ADIF)); /* Wait for conversion to complete, ADIF becomes '1' */
	SET_BIT(ADCSRA,ADIF); /* Clear ADIF by write '1' to it :) */
	return ADC; /* Read the digital value from the data register */
}
